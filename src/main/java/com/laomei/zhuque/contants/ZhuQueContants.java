package com.laomei.zhuque.contants;

/**
 * @author luobo
 */
public interface ZhuQueContants {
    /**
     *  a gt b ==> a > b
     */
    String GT = "gt";

    /**
     * a gte b ==> a >= b
     */
    String GTE = "gte";

    /**
     * a lt b ==> a < b
     */
    String LT = "lt";

    /**
     * a lte b ==> a <= b
     */
    String LTE = "lte";
}
